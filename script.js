// Smooth reveal animation for stats
const statsObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const statNumber = entry.target.querySelector('h3');
            const finalNumber = statNumber.textContent;
            
            if (finalNumber.includes('.')) {
                // For GPA
                animateNumber(statNumber, 0, parseFloat(finalNumber), 1000, 2);
            } else {
                // For other numbers
                const num = parseInt(finalNumber.replace('+', ''));
                animateNumber(statNumber, 0, num, 1000, 0, finalNumber.includes('+'));
            }
        }
    });
}, { threshold: 0.5 });

function animateNumber(element, start, end, duration, decimals = 0, hasPlus = false) {
    const startTime = performance.now();
    
    function update(currentTime) {
        const elapsed = currentTime - startTime;
        const progress = Math.min(elapsed / duration, 1);
        
        const current = start + (end - start) * easeOutQuart(progress);
        const displayValue = decimals > 0 ? current.toFixed(decimals) : Math.floor(current);
        
        element.textContent = displayValue + (hasPlus ? '+' : '');
        
        if (progress < 1) {
            requestAnimationFrame(update);
        }
    }
    
    requestAnimationFrame(update);
}

function easeOutQuart(t) {
    return 1 - Math.pow(1 - t, 4);
}

// Observe stats for number animation
document.addEventListener('DOMContentLoaded', () => {
    document.querySelectorAll('.stat').forEach(stat => {
        statsObserver.observe(stat);
    });
});

